function roundStat(value){return Math.max(1,Math.round(value))}
function makeSpecies(row,rarity,index){
  const[id,name,emoji,element,race,role,minFloor,growthType]=row;
  const rarityProfile=RARITY_PROFILE[rarity],growthProfile=GROWTH_PROFILE[growthType],roleProfile=ROLE_PROFILE[role]??ROLE_PROFILE.balanced;
  const tier=rarityProfile.rank;
  const base={
    hp:(50+tier*15+(index%5)*3)*rarityProfile.scale,
    atk:(8+tier*3+(index%4))*rarityProfile.scale,
    def:(6+tier*2+(index%3))*rarityProfile.scale,
    spd:(9+tier+(index%6))*Math.sqrt(rarityProfile.scale)
  };
  const[skillName,skillDescription]=ROLE_SKILL[role]??ROLE_SKILL.balanced;
  const fieldEncounter=rarity!=="神話";
  return{
    id,name,emoji,element,race,role,rarity,minFloor,fieldEncounter,
    acquisition:fieldEncounter?["探索","召喚","闇市場"]:["神話召喚","闇市場"],
    captureRate:rarityProfile.captureRate,
    maxMp:rarityProfile.maxMp+(index%7),
    growthType,
    growthLabel:growthProfile.label,
    expRate:growthProfile.expRate,
    growth:growthProfile.growth,
    baseStats:{
      hp:roundStat(base.hp*roleProfile.hp),
      atk:roundStat(base.atk*roleProfile.atk),
      def:roundStat(base.def*roleProfile.def),
      spd:roundStat(base.spd*roleProfile.spd),
      crit:Math.max(3,5+tier+(["assassin","critical","ambush"].includes(role)?7:0)+(index%4)),
      evasion:Math.max(1,3+Math.floor(tier/2)+(["speed","assassin"].includes(role)?6:0)+(index%3))
    },
    rankNames:[name,`上位${name}`,`${name}王`,`深淵${name}`],
    skills:[{id:`${id}_skill`,name:skillName,unlock:{type:"level",value:1},description:skillDescription}]
  };
}

